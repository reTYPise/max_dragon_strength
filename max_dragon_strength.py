import sys

def calculate_max_dragon_strength(n: int) -> int:
    """
    Вычисляет максимально возможную силу стаи драконов при заданном общем количестве голов N.
    Сила определяется как произведение количества голов каждого дракона.
    Ограничение: у одного дракона не более 7 голов.
    
    Математическое обоснование:
    Для максимизации произведения при фиксированной сумме числа следует разбивать на 3.
    Числа больше 4 выгодно разбивать (например, 5 -> 2*3=6, 6 -> 3*3=9, 7 -> 3*4=12).
    Число 4 нейтрально (2*2=4).
    Таким образом, оптимальное разбиение состоит только из 2 и 3.
    Ограничение <= 7 не влияет на стратегию, так как 2 и 3 <= 7.
    """
    if n <= 0:
        return 0
    if n == 1:
        return 1 # Единственный вариант: один дракон с 1 головой (произведение 1)
    if n == 2:
        return 2 # Один дракон с 2 головами
    if n == 3:
        return 3 # Один дракон с 3 головами
    
    # Количество троек
    count_threes = n // 3
    remainder = n % 3
    
    if remainder == 0:
        # Все делится на тройки: 3 * 3 * ... * 3
        return 3 ** count_threes
    elif remainder == 1:
        # Остаток 1 невыгоден. Берем одну тройку назад (3+1=4) и разбиваем на 2*2.
        # Итого: (count_threes - 1) троек и две двойки (произведение 4)
        if count_threes == 0:
            # Случай n=4 (изначально count_threes=1, rem=1, но если бы n=1, обработано выше)
            # Для n=4: 3//3 = 1, rem=1. Формула: 3^0 * 4 = 4. Верно.
            return 4 
        return (3 ** (count_threes - 1)) * 4
    else: # remainder == 2
        # Остаток 2 просто добавляем как множитель
        return (3 ** count_threes) * 2

def main():
    try:
        # Чтение входных данных
        # Предполагается, что вход подается через stdin или может быть заменен на input()
        input_data = sys.stdin.read().strip()
        if not input_data:
            # Для локального тестирования, если stdin пуст
            print("Введите число N (0 < N < 100):")
            input_data = input().strip()
            
        n = int(input_data)
        
        if not (0 < n < 100):
            raise ValueError("Число N должно быть в диапазоне 0 < N < 100")
            
        result = calculate_max_dragon_strength(n)
        
        # Вывод результата
        print(result)
        
    except ValueError as e:
        print(f"Ошибка ввода: {e}")
    except Exception as e:
        print(f"Произошла непредвиденная ошибка: {e}")

if __name__ == "__main__":
    main()
